package com.ideas2it.programs;

public class NMinusSolver {

    public static void main(String[] args) {
        int c = 0;
        int d = 0;

        int a = 4;

        for (int i = 0; i < a; i++) {
            c = 0;
            for (int j = 0; j < d; j++) {
                c++;
            }
            d++;
        }
        System.out.println(c);
    }
}
